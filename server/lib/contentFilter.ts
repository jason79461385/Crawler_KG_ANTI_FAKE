// 過濾「不是真實詐騙案例」的新聞 / 文章
//
// 我們希望系統的索引只包含「能當作詐騙腳本參考」的內容,
// 不要納入:
//   - 政治攻防(把對手政策罵成「詐騙」的口水戰)
//   - 政府宣導 / 教育課程 / 反詐倡議
//   - 統計排行 / 趨勢數據
//   - 純政策評論(不含具體案例)
//
// 規則順序:
//   1. 任何 BLOCK 規則命中 → 立刻 reject
//   2. 沒命中 BLOCK,且至少命中一個 ALLOW 信號 → accept
//   3. 都沒命中 → 看內文長度與一般信號,保守 accept

const BLOCK_PATTERNS: Array<{ name: string; regex: RegExp }> = [
  // ── 政治 / 政黨口水 ──────────────────────────
  {
    name: "政黨/政治人物攻防",
    regex:
      /(民眾黨|民進黨|國民黨|時代力量|綠黨|台聯|親民黨|新黨|藍白|綠營|藍營|白營|本土派)/,
  },
  {
    name: "政治動詞(轟/嗆/砲轟)",
    regex:
      /(轟|反嗆|嗆爆|砲轟|怒批|痛批|猛批|反擊|譴責|抨擊|質詢|喊話|嗆聲|互槓|互嗆|放話)/,
  },
  {
    name: "政策 / 選舉",
    regex:
      /(普發|普發[一壹]萬|條例|法案|修法|草案|罷免|大選|競選|連署|公投|提名|初選|選情)/,
  },
  {
    name: "政治職稱(被當主角時)",
    regex:
      /(立委|議員|市長|縣長|總統|副總統|院長|部長|署長|主委|發言人|黨團|黨主席|黨魁)/,
  },

  // ── 政府宣導 / 反詐倡議 / 教育 ───────────────
  {
    name: "防範 / 反詐宣導",
    regex:
      /(防範|防堵|反詐|打詐|杜絕|查緝|呼籲民眾|提醒民眾|警方提醒|警方呼籲|宣導|倡議|教育|培訓|種子|教戰|教學)/,
  },
  {
    name: "政府機關發布",
    regex:
      /(國稅局|教育部|內政部|法務部|金管會|NCC|刑事局|警政署|地方法院|地檢署|消保官|消基會|金融監督)/,
  },
  {
    name: "公部門活動",
    regex:
      /(廉政|操守|誠信職場|宣導講習|防詐講座|反詐研習|防詐音樂會|反詐遊行|防詐宣傳|反詐宣傳|金融消費爭議講座|防詐論壇|反詐論壇|防詐記者會)/,
  },

  // ── 統計 / 排行 ─────────────────────────
  {
    name: "排行 / 統計",
    regex:
      /(詐騙排行|詐騙指數|詐騙名次|詐騙排名|詐騙統計|詐騙人次|詐騙件數|全球.{0,4}詐騙.{0,4}排|亞洲.{0,4}詐騙.{0,4}排|統計顯示.{0,12}詐騙|報告指出.{0,12}詐騙)/,
  },

  // ── 八卦 / 娛樂(與詐騙無關但帶詞)────────────
  {
    name: "演藝 / 娛樂",
    regex:
      /(演藝圈|藝人|偶像|明星|劇情|電影|影集|綜藝|金鐘|金馬|金曲)/,
  },
];

const ALLOW_PATTERNS: RegExp[] = [
  // 第一人稱受害者描述
  /(被騙|遭騙|騙走|騙取|被詐|遇詐|受害|上當|落入)/,
  // 損失 / 金額
  /(損失|失去|騙走|血本無歸|匯出|匯款|轉帳)\s?\d|\d{1,3}萬|\d{1,3},?\d{3}|百萬|千萬|億元/,
  // 接觸管道(代表是案例描述)
  /(加(他|她|了|入).?LINE|加好友|私訊我|收到簡訊|接到電話|交友軟體|聊天室|Telegram)/,
  // 案例 / 手法描述
  /(集團|車手|機房|首腦|偵破|破獲|查獲|逮捕|起訴|判決|騙術|手法|腳本|劇本)/,
  // 165 / 報案
  /(165|反詐騙專線|報案|詐欺罪|刑法\s?\d)/,
];

export type FilterResult = {
  accept: boolean;
  reason?: string;
};

export function filterScamCandidate(input: {
  title: string;
  content: string;
}): FilterResult {
  const haystack = `${input.title} ${input.content}`;

  for (const rule of BLOCK_PATTERNS) {
    if (rule.regex.test(haystack)) {
      return { accept: false, reason: `BLOCK:${rule.name}` };
    }
  }

  for (const allow of ALLOW_PATTERNS) {
    if (allow.test(haystack)) {
      return { accept: true };
    }
  }

  // 沒命中任何 BLOCK,也沒命中強信號
  // → 若內文夠長(>120 字),保守接受;否則丟掉避免噪音
  if (input.content.length >= 120) {
    return { accept: true };
  }
  return { accept: false, reason: "BLOCK:weak-signal" };
}
